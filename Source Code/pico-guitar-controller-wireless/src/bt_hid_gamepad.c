// ────────────────────────────────────────────────────────────────────────
// HID Report Descriptor
//
// Usage (Game Pad, 0x05) → Windows places this in the game controllers list.
// Report ID 0x01 → required for BLE HIDS multi-report devices.
//
// Report data layout (12 bytes, after stripping the report ID byte):
//   Bytes 0–1:   16 buttons (bit-packed, XInput-compatible masks)
//   Byte  2:     Left trigger  (0–255)
//   Byte  3:     Right trigger (0–255)
//   Bytes 4–5:   Left stick X  (signed 16-bit) — Whammy
//   Bytes 6–7:   Left stick Y  (signed 16-bit)
//   Bytes 8–9:   Right stick X (signed 16-bit) — unused
//   Bytes 10–11: Right stick Y (signed 16-bit) — Tilt
// ────────────────────────────────────────────────────────────────────────

static const uint8_t hid_report_descriptor[] = {
    0x05, 0x01,        // Usage Page (Generic Desktop)
    0x09, 0x05,        // Usage (Game Pad)
    0xA1, 0x01,        // Collection (Application)

    0x85, 0x01,        //   Report ID (1)  ← required for BLE HIDS

    // ── 16 Buttons ──
    0x05, 0x09,        //   Usage Page (Button)
    0x19, 0x01,        //   Usage Minimum (Button 1)
    0x29, 0x10,        //   Usage Maximum (Button 16)
    0x15, 0x00,        //   Logical Minimum (0)
    0x25, 0x01,        //   Logical Maximum (1)
    0x95, 0x10,        //   Report Count (16)
    0x75, 0x01,        //   Report Size (1)
    0x81, 0x02,        //   Input (Data, Variable, Absolute)

    // ── Triggers (2 × 8-bit unsigned) ──
    0x05, 0x02,        //   Usage Page (Simulation Controls)
    0x09, 0xC5,        //   Usage (Brake)       — Left Trigger
    0x09, 0xC4,        //   Usage (Accelerator) — Right Trigger
    0x15, 0x00,        //   Logical Minimum (0)
    0x26, 0xFF, 0x00,  //   Logical Maximum (255)
    0x95, 0x02,        //   Report Count (2)
    0x75, 0x08,        //   Report Size (8)
    0x81, 0x02,        //   Input (Data, Variable, Absolute)

    // ── Analog Sticks (4 × 16-bit signed) ──
    0x05, 0x01,        //   Usage Page (Generic Desktop)
    0x09, 0x30,        //   Usage (X)  — Left Stick X / Whammy
    0x09, 0x31,        //   Usage (Y)  — Left Stick Y
    0x09, 0x33,        //   Usage (Rx) — Right Stick X (unused)
    0x09, 0x34,        //   Usage (Ry) — Right Stick Y / Tilt
    0x16, 0x00, 0x80,  //   Logical Minimum (−32768)
    0x26, 0xFF, 0x7F,  //   Logical Maximum (+32767)
    0x95, 0x04,        //   Report Count (4)
    0x75, 0x10,        //   Report Size (16)
    0x81, 0x02,        //   Input (Data, Variable, Absolute)

    0xC0               // End Collection
};

// ATT read callback — serves the GAP Device Name characteristic dynamically.
// This lets the GATT device name match whatever name was passed to bt_hid_init()
// (e.g. from g_config.device_name) rather than being a static compile-time string.
// _bt struct is declared below; use a forward declaration here.
#define GAP_DEVICE_NAME_HANDLE 0x0003
// (defined after _bt struct — see att_read_callback below)
static uint16_t att_read_callback(hci_con_handle_t connection_handle,
                                   uint16_t att_handle,
                                   uint16_t offset,
                                   uint8_t *buffer,
                                   uint16_t buffer_size);
//
// Tells nearby devices:
//   - General discoverable, BLE only (BR/EDR not supported flag)
//   - Device name
//   - Advertises HID Service UUID (0x1812) — Windows needs this to
//     classify as a HID device during discovery before pairing
//   - Appearance = 0x03C4 (HID Gamepad) — shows gamepad icon in BT settings
// ────────────────────────────────────────────────────────────────────────

// Appearance value: Gamepad = 0x03C4 (Category 15 = HID, Sub-category 4 = Gamepad)
#define BLE_APPEARANCE_GAMEPAD  0x03C4

// Forward declaration — the name bytes are filled in dynamically
// We build the adv packet in bt_hid_init() once we have the device name.
static uint8_t adv_data[31];
static uint8_t adv_data_len = 0;

static void build_adv_data(const char *device_name) {
    uint8_t name_len = (uint8_t)strlen(device_name);
    if (name_len > 20) name_len = 20;  // cap to fit in 31-byte adv payload

    uint8_t *p = adv_data;

    // Flags: General Discoverable | BR/EDR Not Supported
    *p++ = 0x02;
    *p++ = BLUETOOTH_DATA_TYPE_FLAGS;
    *p++ = 0x06;

    // Complete Local Name
    *p++ = 1 + name_len;
    *p++ = BLUETOOTH_DATA_TYPE_COMPLETE_LOCAL_NAME;
    memcpy(p, device_name, name_len);
    p += name_len;

    // 16-bit Service UUID: HID Service (0x1812)
    *p++ = 0x03;
    *p++ = BLUETOOTH_DATA_TYPE_COMPLETE_LIST_OF_16_BIT_SERVICE_CLASS_UUIDS;
    *p++ = ORG_BLUETOOTH_SERVICE_HUMAN_INTERFACE_DEVICE & 0xFF;
    *p++ = ORG_BLUETOOTH_SERVICE_HUMAN_INTERFACE_DEVICE >> 8;

    // Appearance: Gamepad (0x03C4)
    *p++ = 0x03;
    *p++ = BLUETOOTH_DATA_TYPE_APPEARANCE;
    *p++ = BLE_APPEARANCE_GAMEPAD & 0xFF;
    *p++ = BLE_APPEARANCE_GAMEPAD >> 8;

    adv_data_len = (uint8_t)(p - adv_data);
}

