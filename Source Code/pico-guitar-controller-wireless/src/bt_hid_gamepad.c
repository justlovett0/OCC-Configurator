/*
 * bt_hid_gamepad.c - Bluetooth LE HID Gamepad for Pico W
 *
 * Implements a BLE HID device using BTstack's GATT HIDS profile.
 * This is the same approach used by Santroller and modern wireless
 * controllers (Switch Pro Controller, DualSense, Xbox Series, etc).
 *
 * Why BLE (not Bluetooth Classic):
 *   The original code used Bluetooth Classic HID (BR/EDR + SDP + L2CAP).
 *   Windows 10/11 handles Classic HID well for standard mice/keyboards,
 *   but guitar/instrument subtypes go through a legacy classification path
 *   that consistently misidentifies the device (shows as mouse, keyboard,
 *   or generic gamepad regardless of SDP subclass settings).
 *
 *   BLE HID (HIDS over GATT) bypasses all of that. Windows reads the
 *   GATT HID Service (UUID 0x1812) and the HID Report Descriptor directly,
 *   then classifies the device purely from the descriptor — no SDP, no
 *   CoD subtype, no legacy driver involvement.
 *
 * How it works:
 *   1. CYW43 is initialized (already done by main.c before bt_hid_init)
 *   2. BTstack GATT server is set up with:
 *        - GAP service (device name)
 *        - HIDS service (HID report descriptor + input report characteristic)
 *   3. BLE advertisements are started — Windows/Android/iOS discover the device
 *   4. On connection: host subscribes to the HIDS Input Report characteristic
 *   5. Reports are sent via hids_device_send_input_report() with the report ID
 *      byte stripped (BLE HIDS manages report ID routing in the GATT layer)
 *
 * Report layout (12 bytes sent over BLE, Report ID 0x01 handled by GATT):
 *   Bytes 0–1:   16 buttons (bit-packed, matches XInput button masks)
 *   Byte  2:     Left trigger  (0–255)
 *   Byte  3:     Right trigger (0–255)
 *   Bytes 4–5:   Left stick X  (−32768 to +32767)
 *   Bytes 6–7:   Left stick Y  (−32768 to +32767)
 *   Bytes 4–5:   Left  stick X (−32768 to +32767) — Whammy
 *   Bytes 8–9:   Right stick X (−32768 to +32767) — unused
 *   Bytes 10–11: Right stick Y (−32768 to +32767) — Tilt
 */

